const calculator = {
    plus: function (a, b) {
        console.log (a + b);
    },
    minus: function (a, b) {
        console.log (a - b);
    },
    divide: function (a, b) {
        console.log (a / b);
    },
    multi: function (a, b) {
        console.log (a * b);
    },
    powerOf: function (a, b) {
        console.log (a ** b);
    },
};

calculator.plus (12, 43);
calculator.minus (33, 13);
calculator.divide (50, 5);
calculator.powerOf (2, 4);